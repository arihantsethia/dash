#ifndef IQCOUNT_H_
#define IQCOUNT_H_

#include "bangdb.h"
namespace bangdb
{
class iqcount
{
public:
	virtual void add(const char *s, int len) = 0;

	virtual int list(int span, int **count_list, int *size) = 0;

	virtual LONG_T count() = 0;

	virtual LONG_T count(int span) = 0;

	virtual void foldSlots() = 0;

	virtual void reset() = 0;

	virtual void close() = 0;

	virtual ~iqcount() { }
};
}

#endif /* IQCOUNT_H_ */
